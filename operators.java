class operators
{
public static void main(String[] args)
{
int a=20, b=10;
System.out.println(a+b);
System.out.println(a-b);
System.out.println(a*b);
System.out.println(a/b);
System.out.println(a%b);
System.out.println(a<b);
System.out.println(a>b);
System.out.println(a==b);
System.out.println(a|b);
System.out.println(a&b);
System.out.println(a<b && a>b);
System.out.println(a<b || a>b);
System.out.println(a<<2);
System.out.println(a>>2);
System.out.println((a<b)?a:b);
}
}